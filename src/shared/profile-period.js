function uniqueList(items) {
  return [...new Set((items || []).filter(Boolean))];
}

function getCurrentYearLuck(snapshot) {
  return snapshot?.advanced?.yearLuck?.items?.find((item) => item?.isCurrent) || snapshot?.advanced?.yearLuck?.items?.[0] || null;
}

function translateFocus(focus, variant) {
  return PERIOD_FOCUS_COPY[variant]?.[focus] || PERIOD_FOCUS_COPY[variant]?.["기본 흐름"] || "";
}

function translateSpecialSignal(label, variant, tone) {
  if (!label) return "";

  const specialCopy = SPECIAL_SIGNAL_COPY[variant]?.[tone];
  if (!specialCopy) return "";

  if (label.includes("용신(")) return specialCopy.yong;
  if (label.includes("희신(")) return specialCopy.hee;
  if (label.includes("기신(")) return specialCopy.gi;
  if (label.includes("구신(")) return specialCopy.gu;
  if (label.includes("삼합")) return specialCopy.harmony;
  if (/(충|형|파|해)/.test(label)) return specialCopy.clash;
  return "";
}

function translateSignal(label, variant, tone) {
  const translated = PERIOD_SIGNAL_COPY[variant]?.[tone]?.[label];
  if (translated) return translated;

  const special = translateSpecialSignal(label, variant, tone);
  if (special) return special;

  return variant === "ability" ? label : "";
}

